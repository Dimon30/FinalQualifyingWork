"""
Общие компоненты регуляторов: параметры и наблюдатель производных.

Расширенный наблюдатель производных (уравнения 49 / 73-77 диссертации):

    ζ̂̇1 = ζ̂2 + κ·a1·(ζ1 - ζ̂1)
    ζ̂̇2 = ζ̂3 + κ²·a2·(ζ1 - ζ̂1)
    ζ̂̇3 = ζ̂4 + κ³·a3·(ζ1 - ζ̂1)
    ζ̂̇4 = σ + y4_model + κ⁴·a4·(ζ1 - ζ̂1)
    σ̇   = κ⁵·a5·(ζ1 - ζ̂1)

где y4_model — известная часть 4-й производной выхода (из модели системы),
ζ1 — измеряемый выход (dim-мерный вектор).
"""
from __future__ import annotations
import numpy as np
from dataclasses import dataclass, field


@dataclass
class HighGainParams:
    """Параметры регулятора с большим коэффициентом усиления.

    Атрибуты:
        kappa   — коэффициент усиления наблюдателя κ
                  ВАЖНО: при больших dt уменьшать kappa
                  (кappa=100 → dt≤0.01, kappa=200 → dt≤0.005)
        a       — коэффициенты характеристического полинома наблюдателя
                  (5-кортеж, гурвицев полином a(p)=p⁵+a1p⁴+...+a5)
        gamma   — коэффициенты обратной связи регулятора
                  (5-кортеж: γ1..γ4 для закона управления, γ5 для η-интегратора)
        L       — уровень насыщения sat_L(·)
        ell     — параметр ℓ из условия (63): 0 < ℓ < 1
    """
    kappa: float = 100.0
    a: tuple = (5.0, 10.0, 10.0, 5.0, 1.0)
    gamma: tuple = (1.0, 3.0, 5.0, 3.0, 1.0)
    L: float = 5.0
    ell: float = 0.9


class DerivativeObserver4:
    """Расширенный наблюдатель 4-го порядка (уравнения 73-77 диссертации).

    Оценивает выход ζ̂1..ζ̂4 и дополнительную переменную σ.
    Работает для многомерного выхода (dim > 1), применяя одинаковую
    структуру к каждой компоненте.

    Параметры:
        dim  — размерность выходного вектора (4 для глав 3 и 4)
        p    — HighGainParams с κ, a1..a5
    """

    def __init__(self, dim: int, p: HighGainParams):
        self.dim = dim
        self.p = p
        self.x1 = np.zeros(dim)   # ζ̂1 ≈ ζ1
        self.x2 = np.zeros(dim)   # ζ̂2 ≈ ζ̇1
        self.x3 = np.zeros(dim)   # ζ̂3 ≈ ζ̈1
        self.x4 = np.zeros(dim)   # ζ̂4 ≈ ζ⃛1
        self.sigma = np.zeros(dim) # σ (интегральная компонента)

    def step(self, y: np.ndarray, y4_model: np.ndarray, dt: float) -> None:
        """Один шаг наблюдателя (явный метод Эйлера).

        Аргументы:
            y         — измеряемый выход ζ1 (dim-мерный)
            y4_model  — известная часть 4-й производной (из модели),
                        например W·b·Ū для главы 4
            dt        — шаг времени
        """
        k = self.p.kappa
        a1, a2, a3, a4, a5 = self.p.a
        e = y - self.x1
        self.x1    += dt * (self.x2    + k    * a1 * e)
        self.x2    += dt * (self.x3    + k**2 * a2 * e)
        self.x3    += dt * (self.x4    + k**3 * a3 * e)
        self.x4    += dt * (self.sigma + y4_model + k**4 * a4 * e)
        self.sigma += dt * (k**5 * a5 * e)

    def hat(self):
        """Возвращает (ζ̂1, ζ̂2, ζ̂3, ζ̂4, σ)."""
        return self.x1, self.x2, self.x3, self.x4, self.sigma

    def reset(self):
        """Сбросить состояние наблюдателя."""
        self.x1[:] = 0.0
        self.x2[:] = 0.0
        self.x3[:] = 0.0
        self.x4[:] = 0.0
        self.sigma[:] = 0.0