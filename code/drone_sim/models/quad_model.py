"""
quad_model.py
=============
Физические параметры модели квадрокоптера.

Публичный интерфейс: класс QuadModel — задаёт параметры дрона, которые
используются в динамике (models/dynamics.py) и регуляторе (control/path_following.py).

Математическая основа: уравнения (52-55) диссертации Ким С.А. (2024).

    ṗ = v
    v̇ = (b(φ,θ,ψ) * (u1 + g) - [0, 0, g]) / mass
    [φ̈, θ̈, ψ̈] = [u2 / J_phi, u3 / J_theta, u4 / J_psi]

Значения по умолчанию соответствуют нормализованной модели диссертации
(mass=1, J=1, g=9.81), при которых результаты совпадают с диссертацией.

Пример::

    from drone_sim.models import QuadModel

    # Нормализованная модель (как в диссертации, значения по умолчанию)
    model = QuadModel()

    # Дрон с реалистичными параметрами
    model = QuadModel(
        g=9.81,
        mass=1.5,
        J_phi=0.02,
        J_theta=0.02,
        J_psi=0.04,
    )

Замечание по настройке регулятора:
    При изменении mass и/или J регуляторы, настроенные под нормализованную
    модель (kappa, gamma и т.д.), потребуют перенастройки — их коэффициенты
    нормированы на единичные инерцию и массу.
"""
from __future__ import annotations
from dataclasses import dataclass


@dataclass
class QuadModel:
    """Физические параметры и лётные ограничения квадрокоптера.

    Физические атрибуты:
        g       -- ускорение свободного падения [м/с²]
        mass    -- масса дрона [кг]
        J_phi   -- момент инерции по рысканью (yaw)   [кг·м²]
        J_theta -- момент инерции по тангажу  (pitch)  [кг·м²]
        J_psi   -- момент инерции по крену    (roll)   [кг·м²]

    Лётные ограничения и критерии стабильности:
        max_speed             -- макс. параметрическая скорость V* [рад/с]
        min_speed             -- мин. параметрическая скорость V*
        max_accel             -- макс. линейное ускорение [м/с²]
        max_velocity_norm     -- порог разлёта ||v|| [м/с]; превышение = отказ
        lateral_error_limit   -- предел |e2| (боковая ошибка) [м]
        tangential_error_limit-- предел |e1| (тангенциальная ошибка) [м]
        nan_is_failure        -- True: NaN/Inf в состоянии → отказ

    Соглашение об углах (нестандартное, как в диссертации):
        φ = рысканье (yaw), θ = тангаж (pitch), ψ = крен (roll)
    """
    # --- Физические параметры ---
    g:       float = 9.81  # ускорение свободного падения [м/с²]
    mass:    float = 1.0   # масса [кг]
    J_phi:   float = 1.0   # момент инерции по рысканью [кг·м²]
    J_theta: float = 1.0   # момент инерции по тангажу  [кг·м²]
    J_psi:   float = 1.0   # момент инерции по крену    [кг·м²]

    # --- Лётные ограничения ---
    max_speed:              float = 3.0   # макс. параметрическая скорость V*
    min_speed:              float = 0.3   # мин. параметрическая скорость V*
    max_accel:              float = 1.0   # макс. линейное ускорение [м/с²]
    max_velocity_norm:      float = 6.0   # порог разлёта ||v|| [м/с]
    lateral_error_limit:    float = 0.5   # предел |e2| [м]
    tangential_error_limit: float = 0.7   # предел |e1| [м]
    nan_is_failure:         bool  = True  # NaN/Inf в состоянии → отказ
