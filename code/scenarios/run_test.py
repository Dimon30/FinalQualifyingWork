import sys
sys.path.insert(0, 'code')

import numpy as np
from drone_sim import make_curve, SimConfig, QuadModel, simulate_path_following

# ---------------------------------------------------------------------------
# ВАЖНО: параметризация кривой
# ---------------------------------------------------------------------------
# Регулятор Гл.4 рассчитан на кривые с ||t(s)|| ~ 1–5.
# Для кривой p(s) = [r*cos(s), r*sin(s), ...]  =>  ||t|| = r.
# При большом r (r=130) это 130 м/с дуговой скорости → взрыв.
#
# Правило: если r большой — делить аргумент на r:
#   p(s) = [r*cos(s/r), r*sin(s/r), ...]   =>  ||t|| = 1  (дуговая параметризация)
#
# Устойчивость наблюдателя ближайшей точки (дискретный шаг):
#   H_{n+1} = H_n * (1 - gamma * dt * ||t||^2),  нужно: 0 < gamma * dt * ||t||^2_max < 2
#   => gamma < 2 / (||t||^2_max * dt)
#
# ---------------------------------------------------------------------------
# Эллиптическая спираль: p(s) = [4cos(s), 2sin(s), 0.5s]
# ||t||^2 in [4.25, 16.25]  =>  gamma < 2/(16.25*0.002) ~= 61.5  =>  берём 5
# ---------------------------------------------------------------------------
curve = make_curve(lambda s: np.array([4.0 * np.cos(s), 2.0 * np.sin(s), 0.5 * s]))

x0 = np.zeros(16)
x0[0:3] = np.array([4.0, 0.0, 0.0])   # p(0) = [4, 0, 0] — старт на кривой

cfg = SimConfig(
    quad_model=QuadModel(),
    Vstar=1.0,
    T=40.0,
    dt=0.002,
    x0=x0,
    kappa=200.0,
    gamma=(1., 3., 5., 3., 1.),
    gamma_nearest=5.0,   # < 2 / (||t||^2_max * dt) = 61.5
    zeta0=0.0,
)

result = simulate_path_following(curve, cfg)
result.print_summary()
result.plot("code/out_images/test", prefix="elliptic")

